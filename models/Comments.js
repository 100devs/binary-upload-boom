const mongoose = require("mongoose");   // Import mongoose

const CommentSchema = new mongoose.Schema({ // Create post schema
  title: {
    type: String,
    required: true,
  },
  likes: {
    type: Number,
    required: true,
  },
  post: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Post",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Comment", CommentSchema);  // Export post model
